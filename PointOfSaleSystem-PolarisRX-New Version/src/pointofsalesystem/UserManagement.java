    private void TableUserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableUserMouseClicked
        String fullname=TableUser.getModel().getValueAt(TableUser.getSelectedRow(), 0).toString();
        try{
            pst=con.prepareStatement("SELECT * FROM AccessLevel WHERE fullname='"+fullname+"'");
            rs=pst.executeQuery();
            if(rs.next()){
                FullName.setText(rs.getString("fullname"));
                Username.setText(rs.getString("username"));
                Password.setText(rs.getString("password"));
                String role=rs.getString("role");
                if(role.equals("Admin")){
                    UserType.setSelectedIndex(1);
                }
                else{
                    UserType.setSelectedIndex(2);
                }
                AddButton.setText("UPDATE");
            }
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_TableUserMouseClicked

