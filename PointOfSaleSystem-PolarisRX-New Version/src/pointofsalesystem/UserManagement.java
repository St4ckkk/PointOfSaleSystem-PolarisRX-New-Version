    private void AddUser(){
        try{
           if(AddButton.getText().equals("ADD")){
               pst=con.prepareStatement("SELECT * FROM accesslevel WHERE username='"+Username.getText()+"'");
               rs=pst.executeQuery();
               if(rs.next()){
                   Warnings.setText("Username is already taken!");
               }
               else{
                   pst=con.prepareStatement("INSERT INTO AccessLevel(username,password,fullname,role)"+"VALUES(?,?,?,?)");
                   pst.setString(1, Username.getText());
                   pst.setString(2, Password.getText());
                   pst.setString(3, FullName.getText().toUpperCase());
                   pst.setString(4, UserType.getSelectedItem().toString());
                   if(Username.getText().equals("")){
                       Warnings.setText("Username is Empty!");
                   }
                   else if(!Password.getText().equals(Password1.getText())){
                       Warnings.setText("Password does not match!");
                   }
                   else if(UserType.getSelectedItem().toString().equals("Select User Type")){
                       Warnings.setText("Please select user type!");
                   }
                   else{
                        pst.executeUpdate();
                        JOptionPane.showMessageDialog(null, FullName.getText()+" is successfully added!");
                        showUsers();
                   }
     
               }
               
           }
           else if(AddButton.getText().equals("UPDATE")){
                   pst=con.prepareStatement("UPDATE AccessLevel SET username=?,password=?,fullname=?,role=? WHERE userID='"+userID+"'");
                   pst.setString(1, Username.getText());
                   pst.setString(2, Password.getText());
                   pst.setString(3, FullName.getText().toUpperCase());
                   pst.setString(4, UserType.getSelectedItem().toString());
                   pst.executeUpdate();
                   JOptionPane.showMessageDialog(null, FullName.getText()+" is successfully updated!");
           }
        }
        catch(HeadlessException | SQLException ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    private void showUsers(){
        try{
            pst=con.prepareStatement("SELECT * FROM AccessLevel ORDER BY fullname ASC");
            rs=pst.executeQuery();
            DefaultTableModel userList=(DefaultTableModel)TableUser.getModel();
            userList.setRowCount(0);
            while(rs.next()){
                Object users[]={rs.getString("fullname")};
                userList.addRow(users);
            }
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }
