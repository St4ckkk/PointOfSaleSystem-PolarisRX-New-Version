      if (c == java.awt.event.KeyEvent.VK_DELETE){
          try{
              pst=con.prepareStatement("DELETE FROM AccessLevel WHERE fullname='"+TableUser.getModel().getValueAt(TableUser.getSelectedRow(), 0).toString()+"'");
              pst.executeUpdate();
              Warnings.setText("User deleted");
              showUsers();
          }
          catch(SQLException ex){
              JOptionPane.showMessageDialog(null, ex);
          }
      }
